let indiceActual = 0;

function mostrarPalabra(index) {
  const p = palabras[index];
  document.getElementById("imagenPalabra").src = p.imagen;
  document.getElementById("palabra").textContent = p.palabra;
  document.getElementById("pronunciacion").textContent = p.pronunciacion;
  document.getElementById("significado").textContent = p.significado;
  document.getElementById("uso").textContent =`Uso: ${p.uso}`;
  document.getElementById("uso_espanol").textContent =`Traduccion: ${p.uso_espanol}`;
}

function cambiarPalabraAnimado(nuevoIndice, direccion) {
  const cuadro = document.getElementById("cuadroPalabra");

  let salida, entrada;
  if (direccion === "adelante") {
    salida = "deslizar-salida-izquierda";
    entrada = "deslizar-entrada-derecha";
  } else if (direccion === "atras") {
    salida = "deslizar-salida-derecha";
    entrada = "deslizar-entrada-izquierda";
  }

  cuadro.classList.remove("deslizar-entrada-izquierda", "deslizar-entrada-derecha", "visible", "deslizar-salida-izquierda", "deslizar-salida-derecha");
  cuadro.classList.add(salida);

  setTimeout(() => {
    indiceActual = nuevoIndice;
    mostrarPalabra(indiceActual);

    cuadro.classList.remove(salida);
    cuadro.classList.add(entrada);

    setTimeout(() => {
      cuadro.classList.remove(entrada);
      cuadro.classList.add("visible");
    }, 50);
  }, 300);
}

function siguientePalabra() {
  const nuevo = (indiceActual + 1) % palabras.length;
  cambiarPalabraAnimado(nuevo, "adelante");
}

function anteriorPalabra() {
  const nuevo = (indiceActual - 1 + palabras.length) % palabras.length;
  cambiarPalabraAnimado(nuevo, "atras");
}

function buscarPalabra() {
  const texto = document.getElementById("busqueda").value.toLowerCase().trim();
  const index = palabras.findIndex(p => p.palabra.toLowerCase() === texto);
  if (index !== -1) {
    indiceActual = index;
    mostrarPalabra(indiceActual);
  }
}

window.onload = () => {
  mostrarPalabra(indiceActual);
  document.getElementById("cuadroPalabra").classList.add("visible");
};